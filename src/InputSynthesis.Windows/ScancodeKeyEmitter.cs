using SharpSticks.InputAbstractions.Keyboard;

namespace SharpSticks.InputSynthesis.Windows;

/// <summary>
/// Emits HID keyboard-page (0x07) keys as PS/2 Set-1 scancodes via
/// <c>SendInput</c> with <c>KEYEVENTF_SCANCODE</c>. Scancode injection is what
/// DirectInput-based games actually read, so it's the right channel for the
/// keyboard page. Keys with no single-scancode representation (Pause, the
/// PrintScreen sequence) and any usage outside the table throw
/// <see cref="NotSupportedException"/>.
/// </summary>
internal static class ScancodeKeyEmitter
{
	public static bool CanEmit(int usage) => TryGetScancode(usage) is not null;

	// HID keyboard usage (0x07 page) -> (PS/2 Set-1 make code, E0-extended).
	private static (ushort Scan, bool Extended)? TryGetScancode(int usage) => usage switch
	{
		// Letters
		0x04 => (0x1E, false), 0x05 => (0x30, false), 0x06 => (0x2E, false), 0x07 => (0x20, false),
		0x08 => (0x12, false), 0x09 => (0x21, false), 0x0A => (0x22, false), 0x0B => (0x23, false),
		0x0C => (0x17, false), 0x0D => (0x24, false), 0x0E => (0x25, false), 0x0F => (0x26, false),
		0x10 => (0x32, false), 0x11 => (0x31, false), 0x12 => (0x18, false), 0x13 => (0x19, false),
		0x14 => (0x10, false), 0x15 => (0x13, false), 0x16 => (0x1F, false), 0x17 => (0x14, false),
		0x18 => (0x16, false), 0x19 => (0x2F, false), 0x1A => (0x11, false), 0x1B => (0x2D, false),
		0x1C => (0x15, false), 0x1D => (0x2C, false),
		// Digits 1-9, 0
		0x1E => (0x02, false), 0x1F => (0x03, false), 0x20 => (0x04, false), 0x21 => (0x05, false),
		0x22 => (0x06, false), 0x23 => (0x07, false), 0x24 => (0x08, false), 0x25 => (0x09, false),
		0x26 => (0x0A, false), 0x27 => (0x0B, false),
		// Whitespace / editing
		0x28 => (0x1C, false), 0x29 => (0x01, false), 0x2A => (0x0E, false), 0x2B => (0x0F, false),
		0x2C => (0x39, false),
		// Punctuation
		0x2D => (0x0C, false), 0x2E => (0x0D, false), 0x2F => (0x1A, false), 0x30 => (0x1B, false),
		0x31 => (0x2B, false), 0x33 => (0x27, false), 0x34 => (0x28, false), 0x35 => (0x29, false),
		0x36 => (0x33, false), 0x37 => (0x34, false), 0x38 => (0x35, false),
		// Caps + function row
		0x39 => (0x3A, false),
		0x3A => (0x3B, false), 0x3B => (0x3C, false), 0x3C => (0x3D, false), 0x3D => (0x3E, false),
		0x3E => (0x3F, false), 0x3F => (0x40, false), 0x40 => (0x41, false), 0x41 => (0x42, false),
		0x42 => (0x43, false), 0x43 => (0x44, false), 0x44 => (0x57, false), 0x45 => (0x58, false),
		// Navigation cluster (E0-extended)
		0x47 => (0x46, false), // ScrollLock
		0x49 => (0x52, true), 0x4A => (0x47, true), 0x4B => (0x49, true), 0x4C => (0x53, true),
		0x4D => (0x4F, true), 0x4E => (0x51, true),
		0x4F => (0x4D, true), 0x50 => (0x4B, true), 0x51 => (0x50, true), 0x52 => (0x48, true),
		// Keypad
		0x53 => (0x45, false), 0x54 => (0x35, true), 0x55 => (0x37, false), 0x56 => (0x4A, false),
		0x57 => (0x4E, false), 0x58 => (0x1C, true), 0x59 => (0x4F, false), 0x5A => (0x50, false),
		0x5B => (0x51, false), 0x5C => (0x4B, false), 0x5D => (0x4C, false), 0x5E => (0x4D, false),
		0x5F => (0x47, false), 0x60 => (0x48, false), 0x61 => (0x49, false), 0x62 => (0x52, false),
		0x63 => (0x53, false), 0x67 => (0x59, false),
		// Application / menu (E0-extended)
		0x65 => (0x5D, true),
		// F13-F24 (best effort; Set-1 extension range)
		0x68 => (0x64, false), 0x69 => (0x65, false), 0x6A => (0x66, false), 0x6B => (0x67, false),
		0x6C => (0x68, false), 0x6D => (0x69, false), 0x6E => (0x6A, false), 0x6F => (0x6B, false),
		0x70 => (0x6C, false), 0x71 => (0x6D, false), 0x72 => (0x6E, false), 0x73 => (0x6F, false),
		// Modifiers
		0xE0 => (0x1D, false), 0xE1 => (0x2A, false), 0xE2 => (0x38, false), 0xE3 => (0x5B, true),
		0xE4 => (0x1D, true), 0xE5 => (0x36, false), 0xE6 => (0x38, true), 0xE7 => (0x5C, true),
		_ => null,
	};

	public static void Emit(Key key, bool down)
	{
		if (TryGetScancode(key.Usage) is not { } entry)
		{
			throw new NotSupportedException(
				$"No scancode mapping for keyboard HID usage 0x{key.Usage:X2} ({key}).");
		}

		Win32Input.Send(Win32Input.KeyByScancode(entry.Scan, entry.Extended, up: !down));
	}
}
